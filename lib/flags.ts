/**
 * 회색 점(비하이라이트 전체 주유소) 표시 기능 on/off.
 *
 * 회색 점 = 화면 영역 내 "전체 주유소"(가격 유무 무관)를 작은 회색 점으로(줌인 시) 표시 + 탭 시 상세.
 * "내 주변(현재 지도 범위)의 모든 주유소 위치"를 한눈에 파악하게 하려는 목적이다.
 * 가격/순위 마커(bbox 가격마커·전국 TOP10·내 주변 TOP10·EXP)로 이미 보이는 주유소는
 * id 기준 제외하고, 나머지만 회색 점으로 그린다(이중 렌더 방지).
 *
 * 줌 게이팅: 동네 수준 확대(카카오 level ≤ GRAY_DOT_MAX_LEVEL=5, 즉 zoom ≥ 10)일 때만 노출.
 * 줌아웃(전국/광역)에서는 미표시(가격 마커만) — 점이 화면을 메우는 것 방지.
 *
 * 데이터 한계: 회색 점은 우리 DB 보유분 기준이라 "전국 전 주유소"가 아닐 수 있다(보유분만 표시).
 *
 * false면 완전 비활성(조회 호출·마커 렌더·범례 안내 모두 미동작).
 * (관련 로직/RPC/route 는 보존 — 이 플래그로만 가드한다.)
 */
export const GRAY_DOTS_ENABLED = true;

/**
 * 초기 베타 전면무료 플래그 — 단일 출처(서버/클라 공용).
 *
 * 켜지면("true") 한꺼번에 적용되는 것:
 *  1) 프리미엄 권한 개방: 로그인 사용자를 프리미엄으로 간주한다.
 *     판정 중앙화 지점은 `lib/auth/options.ts`의 session 콜백 한 곳뿐이다
 *     (서버 getPremiumStatus 결과를 그대로 쓰는 모든 isPremium 분기가 자동으로 열린다:
 *      광고 OFF / 즐겨찾기 동기화 / 가격 변동 푸시 / 헤더 배지 등).
 *  2) 유료/결제 유도 진입점 숨김: 마이페이지 /pricing CTA, 헤더 프리미엄 배지,
 *     가입화면 "유료 결제 이용약관" 강조 등(컴포넌트에서 이 플래그로 분기).
 *
 * 끄면("false" 또는 미설정) 기존(유료) 동작으로 완전 원복된다.
 * → 베타 종료 시 .env의 NEXT_PUBLIC_BETA_FREE 를 false 로 바꾸면 끝(코드 변경 불필요).
 *
 * 주의:
 *  - 결제/구독/PortOne 인프라 코드는 이 플래그와 무관하게 그대로 살아 있다(노출/판정만 우회).
 *  - 비로그인 사용자는 개방 대상이 아니다(계정이 필요한 기능 특성상 로그인은 유지).
 *  - NEXT_PUBLIC_ 접두사라 서버/클라 양쪽에서 동일하게 읽힌다(빌드 타임 인라인).
 */
